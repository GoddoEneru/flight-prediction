def acquisition(column, month, crsdeptime, reporting_airline, dayofweek, orig, dest):
    lis = np.zeros(len(column))
    arr = column
    month_pos = np.where(arr == 'Month')
    crs_pos = np.where(arr == 'CRSDepTime')
    ra_pos = np.where(arr == reporting_airline)
    dow_pos = np.where(arr == 'DayOfWeek')
    orig_pos = np.where(arr == orig)
    dest_pos = np.where(arr == dest)

    lis[month_pos[0]] = month
    lis[crs_pos[0]] = crsdeptime
    lis[ra_pos[0]] = 1
    lis[dow_pos[0]] = dayofweek
    lis[orig_pos[0]] = 1
    lis[dest_pos[0]] = 1
    # return lis
    return pd.DataFrame(data=lis.reshape(1, len(columns)), columns=columns)
